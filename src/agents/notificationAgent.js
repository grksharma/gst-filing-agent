// ─────────────────────────────────────────────
// Save to Google Drive
// ─────────────────────────────────────────────

async function saveToGoogleDrive(fileName, content, mimeType, drive) {
  try {
    const folder = await drive.getOrCreateFolder(GDRIVE_FOLDER_NAME);
    const fileId = await drive.uploadFile({
      name: fileName,
      mimeType,
      content,
      parentId: folder.id,
    });
    return { ok: true, fileId, driveUrl: `https://drive.google.com/file/d/${fileId}/view` };
  } catch (err) {
    return { ok: false, error: err.message };
  }
}

// ─────────────────────────────────────────────
// Save filing record to local storage (offline backup)
// ─────────────────────────────────────────────

async function saveLocalRecord(filingResult, profile, period, summary) {
  const key = `receipt_${profile.gstin}_${filingResult.returnType}_${period.year}_${period.month}`;
  const record = {
    arn: filingResult.arn,
    returnType: filingResult.returnType,
    filedAt: filingResult.filedAt,
    period,
    summary,
    gstin: profile.gstin,
    tradeName: profile.tradeName,
  };
  await AsyncStorage.setItem(key, JSON.stringify(record));
  return record;
}

export async function getFilingHistory(gstin) {
  const keys = await AsyncStorage.getAllKeys();
  const receiptKeys = keys.filter(k => k.startsWith(`receipt_${gstin}_`));
  const records = await AsyncStorage.multiGet(receiptKeys);
  return records.map(([, v]) => JSON.parse(v)).sort((a, b) => new Date(b.filedAt) - new Date(a.filedAt));
}

// ─────────────────────────────────────────────
// Main agent entry point
// ─────────────────────────────────────────────

export async function runNotificationAgent({ filingResult, profile, period, summary, returnType, driveAccessToken }) {
  const results = { push: null, drive: null, local: null, reminder: null };

  // 1. Save local record first (always — offline safety)
  try {
    results.local = await saveLocalRecord(filingResult, profile, period, summary);
  } catch (err) {
    results.localError = err.message;
  }

  // 2. Send immediate push notification
  try {
    await sendPushNotification(
      `${returnType} filed ✓`,
      `ARN: ${filingResult.arn} · ${period.label}`,
      { screen: 'Receipt', arn: filingResult.arn }
    );
    results.push = { ok: true };
  } catch (err) {
    results.push = { ok: false, error: err.message };
  }

  // 3. Save to Google Drive if token available
  if (driveAccessToken) {
    const drive = getDriveService(driveAccessToken);
    const htmlContent = buildReceiptHTML(filingResult, profile, period, summary);
    const fileName = `${profile.gstin}_${returnType}_${period.year}_${String(period.month).padStart(2, '0')}_ARN_${filingResult.arn}.html`;

    results.drive = await saveToGoogleDrive(fileName, htmlContent, 'text/html', drive);

    // Also save the raw payload JSON for audit
    const payloadFileName = `${profile.gstin}_${returnType}_${period.year}_${String(period.month).padStart(2, '0')}_payload.json`;
    await saveToGoogleDrive(payloadFileName, JSON.stringify(filingResult.acknowledgement, null, 2), 'application/json', drive);
  } else {
    results.drive = { ok: false, skipped: true, message: 'Google Drive not connected — receipt saved locally only.' };
  }

  // 4. Schedule next period reminder
  try {
    const nextPeriod = { ...period, month: period.month === 12 ? 1 : period.month + 1, year: period.month === 12 ? period.year + 1 : period.year };
    const reminderId = await scheduleDueDateReminder(returnType, nextPeriod, profile.filingFrequency);
    results.reminder = { ok: true, reminderId, nextPeriod };
  } catch (err) {
    results.reminder = { ok: false, error: err.message };
  }

  return {
    ok: true,
    arn: filingResult.arn,
    results,
    message: `Filing complete. ARN ${filingResult.arn} saved${results.drive?.ok ? ' to Google Drive and' : ''} locally.`,
  };
}
