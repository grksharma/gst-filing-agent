// src/agents/notificationAgent.js
// Agent 7 — Notification & Record Storage
// Stores ARN + PDF to Google Drive (user's own account).
// Sends push notification. Schedules next due-date reminder.
// All data stays in the user's own Google Drive — no Anthropic/app servers.

import * as Notifications from 'expo-notifications';
import * as FileSystem from 'expo-file-system';
import AsyncStorage from '@react-native-async-storage/async-storage';
import dayjs from 'dayjs';
import { GDRIVE_FOLDER_NAME, FILING_TYPES } from '../constants/gst';
import { getDriveService } from '../services/googleDriveService';

// ─────────────────────────────────────────────
// Push notification setup
// ─────────────────────────────────────────────

Notifications.setNotificationHandler({
  handleNotification: async () => ({
    shouldShowAlert: true,
    shouldPlaySound: false,
    shouldSetBadge: true,
  }),
});

export async function requestNotificationPermission() {
  const { status } = await Notifications.requestPermissionsAsync();
  return status === 'granted';
}

async function sendPushNotification(title, body, data = {}) {
  await Notifications.scheduleNotificationAsync({
    content: { title, body, data, sound: false },
    trigger: null, // immediate
  });
}

// ─────────────────────────────────────────────
// Due-date reminder scheduling
// ─────────────────────────────────────────────

export async function scheduleDueDateReminder(returnType, period, filingFrequency) {
  const cfg = FILING_TYPES[returnType]?.dueDates;
  if (!cfg) return;

  const day = filingFrequency === 'quarterly' ? (cfg.quarterly ?? cfg.monthly) : cfg.monthly;
  const dueDate = dayjs().month(period.month).year(period.year).date(day);
  const reminderDate = dueDate.subtract(3, 'day').hour(9).minute(0).second(0);

  if (reminderDate.isBefore(dayjs())) return; // past — skip

  const existingId = await AsyncStorage.getItem(`reminder_${returnType}_${period.year}_${period.month}`);
  if (existingId) await Notifications.cancelScheduledNotificationAsync(existingId).catch(() => {});

  const id = await Notifications.scheduleNotificationAsync({
    content: {
      title: `${returnType} due in 3 days`,
      body: `Your ${returnType} for ${period.label} is due on ${dueDate.format('DD MMM YYYY')}. Open the app to file.`,
      data: { returnType, period },
      sound: false,
    },
    trigger: { date: reminderDate.toDate() },
  });

  await AsyncStorage.setItem(`reminder_${returnType}_${period.year}_${period.month}`, id);
  return id;
}

